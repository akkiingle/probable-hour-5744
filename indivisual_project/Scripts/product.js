function addcart(el,index){
    let x=el.ID;
    for(let i=0;i<cartArr.length;i++){
        if(cartArr[i].ID===x){
            alert("Already added to cart")
            return;
        }
    }
        cartArr.push(el);
        alert("Successfully added to cart");
        localStorage.setItem("cartItems",JSON.stringify(cartArr));   
}

